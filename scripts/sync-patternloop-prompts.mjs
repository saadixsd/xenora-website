#!/usr/bin/env node
/**
 * Sync PatternLoop bundled LoopSpecs → Nora edge prompts.
 * Run after editing patternloop/src/patternloop/bundled/*.yaml:
 *   npm run sync:prompts
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'yaml';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const BUNDLED = path.join(ROOT, 'patternloop/src/patternloop/bundled');
const OUT = path.join(ROOT, 'supabase/functions/_shared/agentPrompts.ts');

const MAPPING = {
  content: 'content_outline.yaml',
  lead: 'lead_qualifier.yaml',
  research: 'research_digest.yaml',
};

/** Strip tool-call instructions Nora does not run in cloud v1. */
function trimPlannerHints(text) {
  return text
    .replace(/TOOL_CALL:[\s\S]*?(?=\n\n|\nFinish|$)/gi, '')
    .replace(/list_dir|read_file/gi, (m) => m)
    .trim();
}

function esc(s) {
  return JSON.stringify(s);
}

function loadSpec(filename) {
  const raw = fs.readFileSync(path.join(BUNDLED, filename), 'utf8');
  return yaml.parse(raw);
}

/** Nora JSON outputs use different surface text than PatternLoop FINAL: markdown. */
const NORA_SUCCESS_KEYWORDS = {
  content: ['x_post', 'hooks', 'linkedin_post', 'cta'],
  lead: ['lead_summary', 'score_rationale', 'reply_draft', 'follow_up'],
  research: ['pain_signals', 'content_angles', 'caveats'],
};

const bundles = {};
for (const [kind, file] of Object.entries(MAPPING)) {
  const spec = loadSpec(file);
  bundles[kind] = {
    patternName: spec.name,
    systemPreamble: spec.system_prompt?.trim() ?? '',
    plannerHints: trimPlannerHints(spec.planner_prompt?.trim() ?? ''),
    evaluatorPrompt: spec.evaluator_prompt?.trim() ?? '',
    successKeywords: NORA_SUCCESS_KEYWORDS[kind] ?? spec.success?.keywords ?? [],
    metaDescription: spec.meta?.description ?? '',
  };
}

const header = `/** AUTO-GENERATED by scripts/sync-patternloop-prompts.mjs — do not edit by hand. */
/** Source: patternloop/src/patternloop/bundled/{content_outline,lead_qualifier,research_digest}.yaml */

export type AgentKind = "content" | "lead" | "research";

export type AgentPromptBundle = {
  patternName: string;
  systemPreamble: string;
  plannerHints: string;
  evaluatorPrompt: string;
  successKeywords: string[];
  metaDescription: string;
};

export const AGENT_PROMPTS: Record<AgentKind, AgentPromptBundle> = {
  content: {
    patternName: ${esc(bundles.content.patternName)},
    systemPreamble: ${esc(bundles.content.systemPreamble)},
    plannerHints: ${esc(bundles.content.plannerHints)},
    evaluatorPrompt: ${esc(bundles.content.evaluatorPrompt)},
    successKeywords: ${JSON.stringify(bundles.content.successKeywords)},
    metaDescription: ${esc(bundles.content.metaDescription)},
  },
  lead: {
    patternName: ${esc(bundles.lead.patternName)},
    systemPreamble: ${esc(bundles.lead.systemPreamble)},
    plannerHints: ${esc(bundles.lead.plannerHints)},
    evaluatorPrompt: ${esc(bundles.lead.evaluatorPrompt)},
    successKeywords: ${JSON.stringify(bundles.lead.successKeywords)},
    metaDescription: ${esc(bundles.lead.metaDescription)},
  },
  research: {
    patternName: ${esc(bundles.research.patternName)},
    systemPreamble: ${esc(bundles.research.systemPreamble)},
    plannerHints: ${esc(bundles.research.plannerHints)},
    evaluatorPrompt: ${esc(bundles.research.evaluatorPrompt)},
    successKeywords: ${JSON.stringify(bundles.research.successKeywords)},
    metaDescription: ${esc(bundles.research.metaDescription)},
  },
};

export const NORA_CONTENT_JSON_CONTRACT = \`You are Nora — XenoraAI's agentic ops co-founder for content. You turn a founder's raw thought into a publishable pack inside the dashboard; the human reviews before anything goes live.

Output EXACTLY this JSON structure (no markdown, no code fences):
{
  "x_post": "A single X/Twitter post (max 280 chars, punchy, no hashtags)",
  "hooks": ["Hook 1", "Hook 2", "Hook 3"],
  "linkedin_post": "A LinkedIn post (2-3 paragraphs, professional, no hashtags)",
  "cta": "A call-to-action line"
}

Target audience: founders, operators, marketers, and small teams shipping in public.
Be direct, sharp, no fluff, no emojis.\`;

export const NORA_LEAD_JSON_CONTRACT = \`You are Nora's Lead Follow-up agent — ops-minded execution, not a loose chat. From meeting notes, pasted inbound text, or lead context in the dashboard, produce actionable follow-up the founder can review before anything sends.

Output EXACTLY this JSON (no markdown, no code fences):
{
  "lead_summary": "2-4 sentences: who they are, intent, stage",
  "score_rationale": "Why this lead is warm/cold and what signals matter",
  "reply_draft": "A concise first reply email or DM (professional tone from user settings)",
  "follow_up_48h": "What to send ~48h later if they go quiet",
  "objections_to_watch": "Bullet-style string: likely objections and how to handle"
}

Be specific to the user's input. No placeholders like [Name] unless truly unknown — use "there" or omit.\`;

export const NORA_RESEARCH_JSON_CONTRACT = \`You are Nora's Research agent — the observe-and-synthesize leg of agentic ops. You receive user notes and optional fetched thread/page text (may be partial). Extract pain signals, content angles, and relevance for a founder building in public.

Output EXACTLY this JSON (no markdown, no code fences):
{
  "pain_signals": "Markdown bullet list of distinct pain signals",
  "content_angles": "Markdown bullet list of hooks or post angles",
  "quotes_evidence": "Short quotes or paraphrases tied to evidence (or say what was missing)",
  "relevance_score_rationale": "2-4 sentences: how relevant this is to the user's stated goal and why",
  "caveats": "Gaps, bias, or fetch failures the user should know"
}

If sources failed or are thin, say so in caveats and still infer carefully from user notes.\`;

export function jsonContractFor(kind: AgentKind): string {
  if (kind === "lead") return NORA_LEAD_JSON_CONTRACT;
  if (kind === "research") return NORA_RESEARCH_JSON_CONTRACT;
  return NORA_CONTENT_JSON_CONTRACT;
}

export type CustomAgentContext = {
  name: string;
  mission: string | null;
  guardrails: string | null;
  starter_prompt: string | null;
  output_deliverables: string | null;
  target_user: string | null;
};

export function buildCustomAgentBlock(agent: CustomAgentContext): string {
  const lines: string[] = ["## Custom agent profile"];
  lines.push(\`Name: \${agent.name}\`);
  if (agent.mission?.trim()) lines.push(\`Mission: \${agent.mission.trim()}\`);
  if (agent.target_user?.trim()) lines.push(\`Target user: \${agent.target_user.trim()}\`);
  if (agent.output_deliverables?.trim()) lines.push(\`Deliverables: \${agent.output_deliverables.trim()}\`);
  if (agent.guardrails?.trim()) lines.push(\`Guardrails: \${agent.guardrails.trim()}\`);
  return lines.join("\\n");
}

export function buildSystemPrompt(
  kind: AgentKind,
  opts: { tone: string; goal: string; customAgentBlock?: string },
): string {
  const base = AGENT_PROMPTS[kind];
  const parts = [
    base.systemPreamble,
    "",
    base.plannerHints ? \`## Planning discipline\\n\${base.plannerHints}\` : "",
    "",
    jsonContractFor(kind),
    "",
    \`Tone: \${opts.tone}\`,
    opts.goal ? \`Goal: \${opts.goal}\` : "",
    opts.customAgentBlock ?? "",
  ];
  return parts.filter(Boolean).join("\\n");
}

export function outputsPassKeywordCheck(kind: AgentKind, serialized: string): boolean {
  const keywords = AGENT_PROMPTS[kind].successKeywords;
  if (!keywords.length) return true;
  const lower = serialized.toLowerCase();
  return keywords.every((k) => lower.includes(k.toLowerCase()));
}

export function buildEvaluatorUserMessage(
  kind: AgentKind,
  goal: string,
  serializedOutput: string,
): string {
  return [
    \`Agent kind: \${kind}\`,
    \`Pattern: \${AGENT_PROMPTS[kind].patternName}\`,
    goal ? \`User goal: \${goal}\` : "",
    "Candidate output (JSON):",
    serializedOutput.slice(0, 12000),
  ]
    .filter(Boolean)
    .join("\\n\\n");
}
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header, 'utf8');
console.log(`Wrote ${OUT}`);
